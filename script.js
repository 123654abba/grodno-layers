let currentLang = "ru";
const langButton = document.querySelector(".lang-toggle");
const modal = document.querySelector(".modal");
const modalTitle = document.querySelector("#modal-title");
const modalPeriod = document.querySelector("#modal-period");
const modalText = document.querySelector("#modal-text");
const heroArt = document.querySelector(".hero-art");

function setLanguage(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (translations[lang][key]) {
      node.textContent = translations[lang][key];
    }
  });

  document.querySelectorAll("[data-i18n-svg]").forEach((node) => {
    const key = node.dataset.i18nSvg;
    if (translations[lang][key]) {
      node.textContent = translations[lang][key];
    }
  });

  document.querySelector(".lang-current").textContent = lang.toUpperCase();
  document.querySelector(".lang-next").textContent = lang === "ru" ? "EN" : "RU";
  document.title = lang === "ru"
    ? "Grodno Layers | Гродно: город на слоях времени"
    : "Grodno Layers | Grodno: A City Built in Layers";
}

function openModal(entry) {
  modalTitle.textContent = entry.title;
  modalPeriod.textContent = entry.period;
  modalText.textContent = entry.text;
  modal.classList.add("is-open");
  modal.setAttribute("aria-hidden", "false");
  document.body.classList.add("modal-open");
  document.querySelector(".modal-close").focus();
}

function closeModal() {
  modal.classList.remove("is-open");
  modal.setAttribute("aria-hidden", "true");
  document.body.classList.remove("modal-open");
}

function clearActivePoints() {
  document.querySelectorAll(".route-point.active, .map-point.active").forEach((node) => {
    node.classList.remove("active");
  });
}

function activatePlace(place) {
  clearActivePoints();
  document.querySelectorAll(`[data-place="${place}"]`).forEach((node) => {
    node.classList.add("active");
  });
  openModal(content[currentLang].places[place]);
}

langButton.addEventListener("click", () => {
  setLanguage(currentLang === "ru" ? "en" : "ru");
});

document.querySelectorAll("[data-place]").forEach((node) => {
  node.addEventListener("click", () => activatePlace(node.dataset.place));
  node.addEventListener("keydown", (event) => {
    if (event.key === "Enter" || event.key === " ") {
      event.preventDefault();
      activatePlace(node.dataset.place);
    }
  });
  if (node.classList.contains("map-point")) {
    node.setAttribute("tabindex", "0");
    node.setAttribute("role", "button");
  }
});

document.querySelectorAll("[data-layer-modal]").forEach((button) => {
  button.addEventListener("click", () => {
    const layer = button.dataset.layerModal;
    openModal(content[currentLang].layers[layer]);
  });
});

document.querySelectorAll("[data-close-modal]").forEach((node) => {
  node.addEventListener("click", closeModal);
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && modal.classList.contains("is-open")) {
    closeModal();
  }
});

const revealObserver = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add("visible");
      revealObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.14 });

document.querySelectorAll(".reveal").forEach((node) => revealObserver.observe(node));

window.addEventListener("scroll", () => {
  if (!heroArt) return;
  const offset = Math.min(window.scrollY * 0.08, 42);
  heroArt.style.transform = `translateY(${offset}px)`;
}, { passive: true });

